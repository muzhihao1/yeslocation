/**
 * 获取优化后的文案
 */
export function getOptimizedCopy(key: string): string {
  const value = OptimizedCopy[key as keyof typeof OptimizedCopy];
  if (typeof value === 'string') {
    return value;
  }
  return key;
}

/**
 * 批量获取优化文案
 */
export function getBatchOptimizedCopy(keys: string[]): Record<string, string> {
  const result: Record<string, string> = {};
  keys.forEach(key => {
    result[key] = getOptimizedCopy(key);
  });
  return result;
}